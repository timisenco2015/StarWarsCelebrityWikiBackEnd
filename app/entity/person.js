class Person
{
  constructor()
  {

  }

  setName(name)
  {
      this.name=name;
  }

  getName()
  {
      return this.name;
  }

  setHeight(height)
  {
      this.height = height;
  }

  getHeight()
  {
      return this.height;
  }

  setMass(mass)
  {
      this.mass = mass;
  }

  getMass()
  {
      return this.mass;
  }
  setHairColor(hairColor)
  {
      this.hairColor = hairColor;
  }

  getHairColor()
  {
      return this.hairColor;
  }
  
  setSkinColor(skinColor)
  {
      this.skinColor = skinColor;
  }

  getSkinColor()
  {
      return this.skinColor;
  }


 
  setGender(gender)
  {
      this.gender = gender;
  }

  getGender()
  {
      return this.gender;
  }


  setBirthYear(birthYear)
  {
      this.birthYear = birthYear;
  }

  getBirthYear()
  {
      return this.birthYear;
  }

  setHomePlanet(homePlanet)
  {
    this.homePlanet = homePlanet;
  }

  getHomePlanet()
  {
      return this.homePlanet;
  }

  setSpecies(species)
  {
    this.species = species;
  }

  getSpecies()
  {
      return this.species;
  }

  setFilms(films)
  {
    this.films = films;
  }

  getFilms()
  {
      return this.films;
  }
 
}

module.exports  = Person;

